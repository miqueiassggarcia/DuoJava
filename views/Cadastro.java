    private UsuarioPadrao cadastrar() {
        // Pegando dados dos campos
        String nome = txtNome.getText();
        String email = txtEmail.getText();
        String genero = "";
        if(radioMasculino.isSelected()) {
            genero = "Masculino";
        } else if(radioFeminino.isSelected()) {
            genero = "Feminino";
        } else if(radioOutro.isSelected()) {
            genero = "Outro";
        }
        String senha = txtSenha.getText();
        
        UsuarioPadraoBuilder userPadraoBuilder = new UsuarioPadraoBuilder();
        
        // Criando usuario
        userPadraoBuilder.setName(nome);
        userPadraoBuilder.setEmail(email);
        userPadraoBuilder.setSexo(genero);
        userPadraoBuilder.setHashSenha(senha);
        userPadraoBuilder.setLinkFoto("encurtador.com.br/ioBLU");
        UsuarioPadrao user = userPadraoBuilder.getUser();
        
        return user;
    }
    
